package com.dev.leo.animaltest.data.entity;

public class AnimalEntity {
    private String url;
    private String title;

    public AnimalEntity(String url, String title) {
        this.url = url;
        this.title = title;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
